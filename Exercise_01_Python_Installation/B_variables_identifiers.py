#? Type Conversions
# Int to Float
a = 5
b = float(a)
print(b)  # Output: 5.0

# Float to Int
c = 3.14
d = int(c)
print(d)  # Output: 3

# Int to String
e = 10
f = str(e)
print(f)  # Output: "10"

# String to Int
g = "15"
h = int(g)
print(h)  # Output: 15

# String to Float
i = "3.14"
j = float(i)
print(j)  # Output: 3.14

# Float to String
k = 3.14
l = str(k)
print(l)  # Output: "3.14"

