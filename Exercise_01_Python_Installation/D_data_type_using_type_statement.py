""" EXERCISE 01
    1.(d) Write programs to know data type of a variable usingType statement.
"""

x = 5 # Int
print(type(x))  # Output: <class 'int'>

y = 3.14 # Float
print(type(y))  # Output: <class 'float'>

name = "Alice" # String
print(type(name))  # Output: <class 'str'>

is_student = True # Boolean
print(type(is_student))  # Output: <class 'bool'>


""" --------------[OUTPUT]--------------------

<class 'int'>
<class 'float'>
<class 'str'>
<class 'bool'>

--------------[END-OF-OUTPUT]-------------- """
